// ===================== FUNCTION =====================
function tremerBotao(button) {

    button.classList.add("button-error");

    setTimeout(() => {

        button.classList.remove("button-error");

    }, 300);

}


function mostrarErro(campo) {
    campo.classList.add("input-error");
}

function removerErro(campo) {
    campo.classList.remove("input-error");
}



// ===================== ADD TO CART =====================
document.addEventListener("click", (event) => {

    if (!event.target.classList.contains("add-cart-button")) return;

    const card = event.target.closest(".product-card");
    const index = card.dataset.index;
    const tipo = card.dataset.tipo;

    let valido = true;

    // ======= BOBINA =======
    if (tipo === "bobina") {

        const indexBobina = index.replace("bobina-", "");
        const bobina = bobinas[indexBobina];

        const seletorVoltagem = card.querySelector(".voltagem-container select");
        const voltagem = seletorVoltagem.value;

        if (!voltagem) {
            mostrarErro(seletorVoltagem);
            valido = false;
        } else {
            removerErro(seletorVoltagem);
        }

        if (!valido) {
            tremerBotao(event.target);
            return;
        }

        const itemCarrinho = {
            nome: bobina.nome,
            quantidade: 1,
            detalhes: [
                `Voltagem: ${voltagem}`
            ]
        };

        carrinho.push(itemCarrinho);
        salvarCarrinho();
        renderCarrinho();
        atualizarContadorCarrinho();

        // Resetar
        seletorVoltagem.value = "";

        // ======= VÁLVULA =======
    } else {

        const produto = produtos[index];

        const rosca = card.querySelector(".thread-select").value;
        const bobina = card.querySelector('input[type="radio"]:checked');
        const voltagem = card.querySelector(".voltagem-container select").value;
        const seletorRosca = card.querySelector(".thread-select");
        const bobinaBox = card.querySelector(".bobina-options");
        const seletorVoltagem = card.querySelector(".voltagem-container select");

        if (!rosca) {
            mostrarErro(seletorRosca);
            valido = false;
        } else {
            removerErro(seletorRosca);
        }

        if (!bobina) {
            bobinaBox.classList.add("bobina-error");
            valido = false;
        }

        if (bobina?.value === "sim" && !voltagem) {
            mostrarErro(seletorVoltagem);
            valido = false;
        }

        if (!valido) {
            tremerBotao(event.target);
            return;
        }

        const itemCarrinho = {
            nome: produto.nome,
            quantidade: 1,
            detalhes: [
                `Rosca: ${rosca}`,
                `Bobina: ${bobina.value}`,
                `Voltagem: ${voltagem || "N/A"}`
            ]
        };

        carrinho.push(itemCarrinho);
        salvarCarrinho();
        renderCarrinho();
        atualizarContadorCarrinho();

        // Resetar
        card.querySelector(".thread-select").value = "";
        card.querySelectorAll('input[type="radio"]').forEach(radio => radio.checked = false);
        card.querySelector(".voltagem-container").style.display = "none";
        card.querySelector(".voltagem-container select").value = "";
    }

});




